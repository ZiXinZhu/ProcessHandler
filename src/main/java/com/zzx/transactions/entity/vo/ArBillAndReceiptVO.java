package com.zzx.transactions.entity.vo;

public class ArBillAndReceiptVO {
    private String billNo;
    private String billGroupNo;
    private String receiptNo;
    private String receiptGroupNo;

    public String getBillNo() {
        return billNo;
    }

    public void setBillNo(String billNo) {
        this.billNo = billNo;
    }

    public String getBillGroupNo() {
        return billGroupNo;
    }

    public void setBillGroupNo(String billGroupNo) {
        this.billGroupNo = billGroupNo;
    }

    public String getReceiptNo() {
        return receiptNo;
    }

    public void setReceiptNo(String receiptNo) {
        this.receiptNo = receiptNo;
    }

    public String getReceiptGroupNo() {
        return receiptGroupNo;
    }

    public void setReceiptGroupNo(String receiptGroupNo) {
        this.receiptGroupNo = receiptGroupNo;
    }
}
